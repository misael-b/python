def circularArrayRotation(a, k, queries):
    # Write your code here
    ans = []
    if k == len(a):
        for i in queries:
            ans.append(a[i])
    else:
        index = k % len(a)
        # if index > len(a):
        #     index = index - len(a)
        for i in queries:
            i = i - index
            ans.append(a[i])
    return ans
    

