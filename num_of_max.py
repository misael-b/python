def birthdayCakeCandles(candles):
    # Write your code here
    candles.sort()
    maxNum = candles[-1]
    numOfMax = 0
    for num in range(-1, -len(candles)-1, -1):
        if candles[num] == maxNum:
            numOfMax += 1
    return numOfMax
    

